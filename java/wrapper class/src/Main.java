//
//          boolean             Boolean
//          char                Character
//          int                 Integer
//          double              Double


//         autoboxing = the automatic conversion that the Java compiler makes between the primitive types and their corresponding object wrapper classes
//         unboxing = the reverse of autoboxing. Automatic conversion of wrapper class to primitive


        //reference datatypes
        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double d = 3.14;
        String e = "noje";

        //in formatul asta putem avea mai multe optiuni de lucru de ex e.ceva(metoda)
        //cum sunt metode pentru String asa o sa avem pentru Boolean,Character,Integer etc..
