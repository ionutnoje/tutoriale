import math

pi = 3.14
x = 54
y = 3
z = 1

print(round(pi))    #functia round o sa rotunjeasca valoarea dintre paranteze

print(math.ceil(pi))    #functia math.ceil() o sa rotunjeasca in sus variabila dintre paranteze

print(math.floor(pi))   #functia math.floor() o sa rotunjeasca in jos variabila dintre paranteze

print(abs(pi))  #returneaza valoarea absoluta a valoarii din paranteza

print(pow(pi,2))    #o sa returneze pi la puterea pe care o specificam noi

print(math.sqrt(431))    #face radical din valoarea pe care o dam in paranteze

print(max(x,y,z))   #functie care returneaza valoarea maxima dintre n variabile

print(min(x,y,z))   #functie care returneaza valoarea minima dintre n variabile


